let nome = 'Akira';
console.log(nome);

// A propriedade length retorna o tamanho da string, ou seja, o número de caracteres que a 
//string possui. Esse método será muito útil em laços de repetição, quando iremos implementar 
//um código para percorrer os caracteres de uma string.

console.log(nome.toUpperCase()); // transforma todos os caracteres de uma string para maiúsculos
console.log(nome.toLowerCase()); // transforma para minúsculo
console.log(nome.length); // a propriedade length retorna o tamanho da string, ou seja, o número de caracteres que a string possui
console.log(typeof nome); // verifica o tipo de cada uma das variáveis declaradas
console.log(nome.charAt(4)); // para acessar os caracteres em uma string. Podemos fazer o acesso onde n é o valor da posição do caractere na string